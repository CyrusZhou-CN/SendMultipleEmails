<script lang="tsx">
import { defineComponent, h } from 'vue';
import SvgIcon from '@/components/SvgIcon'; // 确保引入 SvgIcon 组件

export default defineComponent({
  name: 'MenuItem',
  props: {
    icon: {
      type: String,
      default: ''
    },
    title: {
      type: String,
      default: ''
    }
  },
  setup(props) {
    return () => {
      const vnodes = [];

      if (props.icon) {
        if (props.icon.includes('el-icon')) {
          vnodes.push(h('i', { class: [props.icon, 'sub-el-icon'] }));
        } else {
          vnodes.push(h(SvgIcon, { iconClass: props.icon }));
        }
      }

      if (props.title) {
        vnodes.push(h('span', { slot: 'title' }, props.title));
      }

      return vnodes;
    };
  }
});
</script>

<style scoped>
.sub-el-icon {
  color: currentColor;
  width: 1em;
  height: 1em;
}
</style>
